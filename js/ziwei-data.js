  return {
    star(name)         { return STARS[name] || null; },
    auxEffect(name)    { return AUX_EFFECTS[name] || null; },
    huaEffect(hua)     { return HUA_EFFECTS[hua] || null; },
    palaceCtx(palace, qCat) {
      const pc = PALACE_CONTEXTS[palace];
      if (!pc) return '';
      return pc[qCat] || pc.general || '';
    },

    // 取得特定宮位所有星的完整詮釋
    readPalaceFull(palace, qCat) {
      const lines = [];
      // 宮位情境說明
      const pctx = this.palaceCtx(palace.name, qCat);
      if (pctx) lines.push(pctx);
      // 主星詮釋
      for (const s of palace.stars) {
        const sd = STARS[s];
        if (!sd) continue;
        const isWarn = palace.hua.some(h => h.hua === '化忌' && h.star === s);
        const ctx = sd.contexts[qCat] || sd.contexts.general;
        if (ctx) lines.push(`★ ${s}（${sd.summary}）：${isWarn ? ctx.warn : ctx.pos}`);
      }
      // 輔星效應
      for (const s of (palace.auxList || [])) {
        const ae = AUX_EFFECTS[s];
        if (!ae) continue;
        lines.push(ae.warn || ae.good);
      }
      // 四化效應
      for (const h of (palace.hua || [])) {
        const he = HUA_EFFECTS[h.hua];
        if (!he) continue;
        const huaLine = he[qCat] || he.general;
        if (huaLine) lines.push(`◆ ${h.star}${h.hua}：${huaLine}`);
      }
      return lines;
    },

    // 取得星象概要（短版，用於快速描述）
    starSummary(starName, qCat, isWarn) {
      const sd = STARS[starName];
      if (!sd) return '';
      const ctx = sd.contexts[qCat];
      if (!ctx) return sd.summary;
      return isWarn ? ctx.warn : ctx.pos;
    },
  };

